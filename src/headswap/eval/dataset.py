from __future__ import annotations

import json
import math
from pathlib import Path
from typing import Any

import numpy as np
from PIL import Image, ImageDraw


def eval_root(project_root: Path | None = None) -> Path:
    if project_root is None:
        from headswap.config import project_root as pr

        project_root = pr()
    return Path(project_root) / "data" / "eval"


def _draw_person(size: tuple[int, int], seed: int, skin: tuple[int, int, int], hair: tuple[int, int, int]) -> Image.Image:
    rng = np.random.default_rng(seed)
    w, h = size
    img = Image.new("RGB", size, (int(220 + rng.integers(-20, 20)),) * 3)
    draw = ImageDraw.Draw(img)
    # body
    cx = w // 2
    draw.ellipse([cx - w * 0.28, h * 0.42, cx + w * 0.28, h * 1.05], fill=(40, 40, 50))
    # head
    head_r = int(min(w, h) * 0.16)
    hy = int(h * 0.28)
    draw.ellipse([cx - head_r, hy - head_r, cx + head_r, hy + head_r], fill=skin)
    # hair
    draw.ellipse(
        [cx - head_r * 1.1, hy - head_r * 1.35, cx + head_r * 1.1, hy - head_r * 0.1],
        fill=hair,
    )
    # eyes / mouth for "identity"
    eye_y = hy - head_r * 0.15
    draw.ellipse([cx - head_r * 0.45, eye_y, cx - head_r * 0.15, eye_y + head_r * 0.2], fill=(20, 20, 20))
    draw.ellipse([cx + head_r * 0.15, eye_y, cx + head_r * 0.45, eye_y + head_r * 0.2], fill=(20, 20, 20))
    draw.arc(
        [cx - head_r * 0.35, hy + head_r * 0.15, cx + head_r * 0.35, hy + head_r * 0.55],
        start=10,
        end=170,
        fill=(120, 60, 60),
        width=max(2, head_r // 12),
    )
    # shirt text-like mark for body-preserve checks
    draw.rectangle([cx - 40, int(h * 0.62), cx + 40, int(h * 0.7)], fill=(240, 240, 240))
    return img


def generate_synthetic_eval_set(
    root: Path | None = None,
    n_pairs: int = 24,
    hard_ratio: float = 0.35,
) -> Path:
    """
    Create a deterministic synthetic eval set for CI / offline harness.
    Replace bodies/faces with real photos under the same layout for GPU runs.
    """
    root = root or eval_root()
    bodies = root / "bodies"
    faces = root / "faces"
    bodies.mkdir(parents=True, exist_ok=True)
    faces.mkdir(parents=True, exist_ok=True)

    skin_tones = [
        (235, 200, 170),
        (210, 160, 120),
        (160, 110, 80),
        (110, 70, 50),
        (245, 220, 200),
        (190, 140, 100),
    ]
    hair_colors = [
        (20, 15, 10),
        (60, 35, 15),
        (180, 160, 120),
        (30, 30, 40),
        (90, 50, 30),
    ]

    pairs: list[dict[str, Any]] = []
    n_hard = max(1, int(math.ceil(n_pairs * hard_ratio)))
    for i in range(n_pairs):
        pid = f"pair_{i:03d}"
        difficulty = "hard" if i < n_hard else "easy"
        skin_b = skin_tones[i % len(skin_tones)]
        skin_f = skin_tones[(i * 3 + 1) % len(skin_tones)]
        hair_b = hair_colors[i % len(hair_colors)]
        hair_f = hair_colors[(i * 2 + 1) % len(hair_colors)]
        body = _draw_person((512, 640), seed=1000 + i, skin=skin_b, hair=hair_b)
        face = _draw_person((512, 512), seed=5000 + i, skin=skin_f, hair=hair_f)
        if difficulty == "hard":
            # taller / more hair mass on body
            body = _draw_person((480, 720), seed=1000 + i, skin=skin_b, hair=hair_b)
        body_path = bodies / f"{pid}.png"
        face_path = faces / f"{pid}.png"
        body.save(body_path)
        face.save(face_path)
        pairs.append(
            {
                "id": pid,
                "body": str(body_path.relative_to(root)),
                "face": str(face_path.relative_to(root)),
                "difficulty": difficulty,
                "tags": ["synthetic", "long_hair_mismatch" if difficulty == "hard" else "frontal"],
            }
        )

    manifest = {
        "version": 1,
        "description": "Synthetic head-swap eval pairs for harness testing. Swap in real photos for production eval.",
        "n_pairs": len(pairs),
        "pairs": pairs,
    }
    manifest_path = root / "pairs.json"
    manifest_path.write_text(json.dumps(manifest, indent=2))
    (root / "README.md").write_text(
        "# Eval set\n\n"
        "Synthetic pairs are generated by `scripts/prepare_eval_set.py` for CI.\n\n"
        "For real GPU benchmarking, replace `bodies/` and `faces/` with consented photos "
        "and keep the same filenames listed in `pairs.json` (or regenerate the manifest).\n"
        "Prefer diverse: pose, lighting, hair length mismatch, occlusion, skin tone.\n"
    )
    return manifest_path


def custom_root(project_root: Path | None = None) -> Path:
    if project_root is None:
        from headswap.config import project_root as pr

        project_root = pr()
    return Path(project_root) / "data" / "custom"


def prepare_custom_eval_set(
    custom_dir: Path | None = None,
    root: Path | None = None,
    *,
    body_name: str = "body.png",
    face_name: str = "face.png",
    pair_id: str = "custom_001",
) -> Path:
    """
    Build a 1-pair eval set from real photos in data/custom/.

    Expected layout:
      data/custom/body.png  — destination / body image
      data/custom/face.png  — source face / head image

    Copies into the standard eval layout (data/eval/bodies|faces + pairs.json)
    so run_eval / run_compare keep working unchanged.
    """
    from headswap.config import project_root as pr

    custom_dir = Path(custom_dir) if custom_dir else custom_root()
    root = root or eval_root()
    body_src = custom_dir / body_name
    face_src = custom_dir / face_name
    if not body_src.is_file():
        raise FileNotFoundError(
            f"Missing body image: {body_src}\n"
            f"Place the destination photo at data/custom/{body_name}"
        )
    if not face_src.is_file():
        raise FileNotFoundError(
            f"Missing face image: {face_src}\n"
            f"Place the source face photo at data/custom/{face_name}"
        )

    bodies = root / "bodies"
    faces = root / "faces"
    bodies.mkdir(parents=True, exist_ok=True)
    faces.mkdir(parents=True, exist_ok=True)

    # Normalize to RGB PNG so downstream code always gets a consistent format.
    body_dst = bodies / f"{pair_id}.png"
    face_dst = faces / f"{pair_id}.png"
    Image.open(body_src).convert("RGB").save(body_dst)
    Image.open(face_src).convert("RGB").save(face_dst)

    pairs = [
        {
            "id": pair_id,
            "body": str(body_dst.relative_to(root)),
            "face": str(face_dst.relative_to(root)),
            "difficulty": "real",
            "tags": ["custom", "real_photo"],
        }
    ]
    try:
        custom_rel = str(custom_dir.relative_to(pr()))
    except ValueError:
        custom_rel = str(custom_dir)
    manifest = {
        "version": 1,
        "description": (
            f"Custom 1-pair eval set from {custom_rel} "
            f"({body_name} → body, {face_name} → face)."
        ),
        "n_pairs": 1,
        "source_custom_dir": str(custom_dir),
        "pairs": pairs,
    }
    manifest_path = root / "pairs.json"
    manifest_path.write_text(json.dumps(manifest, indent=2))
    (root / "README.md").write_text(
        "# Eval set (custom)\n\n"
        f"Single real pair prepared from `{custom_dir}/`.\n\n"
        f"- body: `{body_name}` → `bodies/{pair_id}.png`\n"
        f"- face: `{face_name}` → `faces/{pair_id}.png`\n\n"
        "Regenerate with:\n"
        "```bash\n"
        "python scripts/prepare_eval_set.py --custom\n"
        "```\n"
    )
    print(f"Prepared 1 custom pair → {manifest_path}")
    print(f"  body: {body_src} → {body_dst}")
    print(f"  face: {face_src} → {face_dst}")
    return manifest_path


def load_pairs(root: Path | None = None) -> list[dict[str, Any]]:
    root = root or eval_root()
    manifest = root / "pairs.json"
    if not manifest.exists():
        generate_synthetic_eval_set(root)
    data = json.loads(manifest.read_text())
    pairs = []
    for p in data["pairs"]:
        pairs.append(
            {
                **p,
                "body_path": root / p["body"],
                "face_path": root / p["face"],
            }
        )
    return pairs


def main_prepare():
    import argparse

    ap = argparse.ArgumentParser(
        description="Prepare head-swap eval pairs (synthetic or custom real photos)."
    )
    ap.add_argument("--n", type=int, default=24, help="Synthetic pair count (ignored with --custom).")
    ap.add_argument("--out", type=str, default=None, help="Eval root (default: data/eval).")
    ap.add_argument(
        "--custom",
        nargs="?",
        const="data/custom",
        default=None,
        help=(
            "Build a 1-pair eval set from real photos. "
            "Optional path to the custom dir (default: data/custom). "
            "Expects body.png + face.png inside."
        ),
    )
    args = ap.parse_args()
    root = Path(args.out) if args.out else None
    if args.custom is not None:
        path = prepare_custom_eval_set(
            custom_dir=_resolve_project_path(Path(args.custom)),
            root=root,
        )
    else:
        path = generate_synthetic_eval_set(root, n_pairs=args.n)
    print(f"Wrote {path}")


def _resolve_project_path(rel: Path) -> Path:
    from headswap.config import project_root as pr

    p = Path(rel)
    if p.is_absolute():
        return p
    return pr() / p


if __name__ == "__main__":
    main_prepare()
